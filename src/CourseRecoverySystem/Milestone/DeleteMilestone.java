    //purpose is that when the user search the student ID it allow the user to delete the entirety of the details
    //of that student
    private void deleteAllByStudentID() {
        String searchID = tfSearchID.getText().trim();
        if (searchID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a student ID.", "WARNING", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Delete ALL records for ID: " + searchID + "?", "Confirm", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            if (deleteByStudentID(searchID)) {
                JOptionPane.showMessageDialog(this, "Records deleted.");
                loadFromFileToTable((DefaultTableModel) tableClients.getModel());
            } else {
                JOptionPane.showMessageDialog(this, "No records found or delete failed.");
            }
        }
    }
    
    //this purpose is when the user dont have to search the student ID by simply click on the table content on the right
    //and able to perform selected row deletion
    private void deleteSelectedRow() {
        int selectedRow = tableClients.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tableClients.getModel();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a milestone.", "No selection", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sID = model.getValueAt(selectedRow, 0).toString().trim();
        String course = model.getValueAt(selectedRow, 1).toString().trim();
        String week = model.getValueAt(selectedRow, 2).toString().trim();
        String task = model.getValueAt(selectedRow, 3).toString().trim();

        int confirm = JOptionPane.showConfirmDialog(this, "Delete this student?", "Confirm", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                List<Milestone> all = repository.loadMilestones();
                List<Milestone> toKeep = new ArrayList<>();
                boolean found = false;

                for (Milestone m : all) {
                    if (m.getStudentID().equals(sID) && 
                        m.getCourse().equals(course) && 
                        m.getStudyWeek().equals(week) && 
                        m.getTask().equals(task) && 
                        !found) { 
                        found = true;
                    } else {
                        toKeep.add(m);
                    }
                }

                if (found) {
                    repository.saveMilestones(toKeep);
                    model.removeRow(selectedRow);
                    JOptionPane.showMessageDialog(this, "Deleted successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "Error: Could not find record in database.");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }
    
