    //this is when the user click on the saved changes
    private void saveChanges() {
    selectedRow = tableClients.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row first!", "No Selection", JOptionPane.ERROR_MESSAGE);
        return;
    }

    String newID = tfStudentID.getText().trim();
    String newCourse = tfCourse.getText().trim();
    String newWeek = tfStudyWeek.getText().trim();
    String newTask = tfTask.getText().trim();
    String newProgress = progressComboBox.getSelectedItem().toString();

    DefaultTableModel model = (DefaultTableModel) tableClients.getModel();
    model.setValueAt(newID, selectedRow, 0);
    model.setValueAt(newCourse, selectedRow, 1);
    model.setValueAt(newWeek, selectedRow, 2);
    model.setValueAt(newTask, selectedRow, 3);
    model.setValueAt(newProgress, selectedRow, 4);

    try {
        List<Milestone> all = repository.loadMilestones();
        all.set(selectedRow, new Milestone(newCourse, newWeek, newTask, newID, newProgress));
        repository.saveMilestones(all);
        JOptionPane.showMessageDialog(this, "Updated successfully!");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Save Error: " + e.getMessage());
    }
}
    
