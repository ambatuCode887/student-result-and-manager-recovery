    //when the user click on the row, then we can click the button called "selected row" and then we can edit the content
    //on the txtfield
    private void loadSelectedRow() {
        selectedRow = tableClients.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tableClients.getModel();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to edit first!", "No selection", JOptionPane.ERROR_MESSAGE);
            return;
        }
        tfStudentID.setText(model.getValueAt(selectedRow, 0).toString());
        tfCourse.setText(model.getValueAt(selectedRow, 1).toString());
        tfStudyWeek.setText(model.getValueAt(selectedRow, 2).toString());
        tfTask.setText(model.getValueAt(selectedRow, 3).toString());
        String progressValue = model.getValueAt(selectedRow, 4).toString();
        progressComboBox.setSelectedItem(progressValue);
}

