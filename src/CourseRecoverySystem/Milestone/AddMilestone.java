    //perfoming validation when adding the detail into the txt files
    private void addMilestone() {
        if(listFailedStudent.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Please select a student from the dropdown list!", "No student Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(listFailedSubject.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Please select a subject from the dropdown list!", "No subject Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String course = listFailedSubject.getSelectedItem().toString();
        String studyWeek = tfStudyWeek.getText();
        String task = tfTask.getText();
        String studentID = tfStudentID.getText();
        String progress = progressComboBox.getSelectedItem().toString();

        if (!validateAllFields(course, studyWeek, task, studentID) || 
            !validateStudentID(studentID)) {
            return;
        }

        if(milestoneExists(course, studyWeek, task, studentID)) {
            JOptionPane.showMessageDialog(this, "This milestone already exists!", "Duplicate Entry", JOptionPane.WARNING_MESSAGE);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tableClients.getModel();
        model.addRow(new Object[]{studentID, course, studyWeek, task, progress});
        appendMilestoneToFile(course, studyWeek, task, studentID, progress);

        JOptionPane.showMessageDialog(this, "Milestone added successfully");
        
        clearFields(tfStudyWeek, tfTask, tfStudentID);
        listFailedStudent.setSelectedIndex(0);
        listFailedSubject.setSelectedIndex(0);
        
        progressComboBox.setSelectedItem("Ongoing");
    }
