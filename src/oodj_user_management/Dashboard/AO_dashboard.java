    public AO_dashboard(UI_Login loginFrame, oodj_user_management.LoginLogEntry logEntry, String welcomeMessage) {
        this(loginFrame, logEntry);
        lblMessage.setText(welcomeMessage);
        lblMessage.setForeground(new java.awt.Color(0, 153, 51)); // Set to green color
    }

    public void refreshWelcomeMessage() {
        if (loginFrame != null && loginFrame.getUsername() != null && loginFrame.getRole() != null) {
            String welcomeMessage = "Login Success! Welcome, " + loginFrame.getUsername() + " (" + loginFrame.getRole()
                    + ").";
            lblMessage.setText(welcomeMessage);
            lblMessage.setForeground(new java.awt.Color(0, 153, 51)); // Ensure green color
            revalidate();
            repaint();
        }
    }

