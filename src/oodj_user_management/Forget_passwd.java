    public void showPasswordResetDialog(String userId) {
        JPasswordField newPassword = new JPasswordField(15);
        JPasswordField confirmPassword = new JPasswordField(15);

        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Enter New Password:"));
        panel.add(newPassword);
        panel.add(new JLabel("Confirm New Password:"));
        panel.add(confirmPassword);

        int result = JOptionPane.showConfirmDialog(this, panel, "Reset Password for " + userId,
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);

        if (result == JOptionPane.OK_OPTION) {
            String newPass = new String(newPassword.getPassword());
            String confirmPass = new String(confirmPassword.getPassword());

            if (newPass.isEmpty() || confirmPass.isEmpty() || !newPass.equals(confirmPass)) {
                JOptionPane.showMessageDialog(this, "Passwords do not match or fields are empty.", "Error",
                        JOptionPane.ERROR_MESSAGE);
                showPasswordResetDialog(userId);
                return;
            }

            String[] currentData = accountHandler.findAccountByID(userId);

            if (currentData == null) {
                JOptionPane.showMessageDialog(this, "System Error: User data lost during reset.", "System Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            currentData[2] = newPass;

            if (accountHandler.updateAccount(currentData)) {
                JOptionPane.showMessageDialog(this, "Password successfully reset! You can now log in.", "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                // Jump back to the login page after success
                jButton1ActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update password due to a system error.", "System Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }

