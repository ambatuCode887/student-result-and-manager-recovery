    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        // Check button, after entering userid and username, if the account exists, the
        // question will be displayed in jlabel5,
        // and then the user will enter the answer and press check to use the option
        // panel to enter newpassword and confirmpassword
        String inputUserID = jTextField1.getText().trim();
        String inputUsername = jTextField2.getText().trim();
        String inputAnswer = jTextField3.getText().trim();

        if (currentUserData == null) {
            if (inputUserID.isEmpty() || inputUsername.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter both UserID and Username.", "Input Required",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            String[] foundData = accountHandler.findAccountByID(inputUserID);

            // Verify Username
            if (foundData != null && foundData.length >= 7 && foundData[1].trim().equalsIgnoreCase(inputUsername)) {

                currentUserData = foundData;
                String question = currentUserData[5].trim();
                jLabel5.setText(question);
                jPanel1.setVisible(true);
                jLabel3.setText("Please answer the security question.");
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(false);
                jTextField3.setText("");
                jButton2.setText("Verify Answer");
            } else {
                JOptionPane.showMessageDialog(this, "Account not found or details are incorrect.",
                        "Verification Failed", JOptionPane.ERROR_MESSAGE);
            }
            return;
        }

        if (currentUserData != null) {
            if (inputAnswer.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter the answer.", "Input Required",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            String storedAnswer = currentUserData[6].trim();

            if (inputAnswer.equalsIgnoreCase(storedAnswer)) {
                // Answer Verification is successful then pops up dialog box for resetting
                // password
                showPasswordResetDialog(inputUserID);
            } else {
                JOptionPane.showMessageDialog(this, "Incorrect answer. Please try again.", "Verification Failed",
                        JOptionPane.ERROR_MESSAGE);
                jTextField3.setText("");
            }
        }
    }// GEN-LAST:event_jButton2ActionPerformed

