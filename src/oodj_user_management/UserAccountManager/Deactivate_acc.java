        private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
                // TODO add your handling code here:
                String targetUserId = jTextField1.getText().trim();

                if (targetUserId.isEmpty() || jLabel4.getText().equals("Userid")) {
                        JOptionPane.showMessageDialog(this, "Please check a UserID first.", "Update Error",
                                        JOptionPane.WARNING_MESSAGE);
                        return;
                }

                String newStatus = (String) jComboBox1.getSelectedItem();

                if (newStatus == null || jComboBox1.getSelectedIndex() == -1) {
                        JOptionPane.showMessageDialog(this, "Please select an account status.", "Input Error",
                                        JOptionPane.WARNING_MESSAGE);
                        return;
                }

                // Get all current information from the display panel
                String[] currentData = accountHandler.findAccountByID(targetUserId);

                if (currentData == null || currentData.length < 7) {
                        JOptionPane.showMessageDialog(this, "System Error: Failed to retrieve user data for update.",
                                        "System Error", JOptionPane.ERROR_MESSAGE);
                        return;
                }

                String message = String.format(
                                "Confirm update status for UserID: %s?\n" +
                                                "New Status: %s",
                                targetUserId,
                                newStatus);

                int confirmation = JOptionPane.showConfirmDialog(this,
                                message, "Confirm Status Update", JOptionPane.YES_NO_OPTION);

                if (confirmation == JOptionPane.YES_OPTION) {

                        // (Status=index 4)
                        currentData[4] = newStatus;

                        if (accountHandler.updateAccount(currentData)) {
                                JOptionPane.showMessageDialog(this, "Account Status updated successfully!", "Success",
                                                JOptionPane.INFORMATION_MESSAGE);
                                jButton3ActionPerformed(evt);
                        } else {
                                JOptionPane.showMessageDialog(this,
                                                "Error: Failed to update status due to a file write error.",
                                                "System Error", JOptionPane.ERROR_MESSAGE);
                        }
                }
        }// GEN-LAST:event_jButton2ActionPerformed

        private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
                // TODO add your handling code here:
                String inputUserID = jTextField1.getText().trim();
                clearAccInfoDisplay();

                if (inputUserID.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Please enter a UserID to check.", "Input Required",
                                        JOptionPane.WARNING_MESSAGE);
                        return;
                }

                String[] userData = accountHandler.findAccountByID(inputUserID);

                if (userData != null && userData.length >= 7) {
                        String userID = userData[0].trim();
                        String username = userData[1].trim();
                        String password = userData[2].trim();
                        String role = userData[3].trim();
                        String status = userData[4].trim();
                        String question = userData[5].trim();
                        String answer = userData[6].trim();

                        jLabel4.setText(userID);
                        jLabel6.setText(username);
                        jLabel8.setText(password);
                        jLabel10.setText(role);
                        jLabel12.setText(status);
                        jLabel14.setText(question);
                        jLabel16.setText(answer);

                        jComboBox1.setSelectedItem(status);

                        JOptionPane.showMessageDialog(this, "User data loaded successfully.", "Success",
                                        JOptionPane.INFORMATION_MESSAGE);

                } else {
                        JOptionPane.showMessageDialog(this,
                                        "UserID '" + inputUserID + "' not found.",
                                        "User Not Found", JOptionPane.WARNING_MESSAGE);
                }
        }// GEN-LAST:event_jButton3ActionPerformed

